#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*joined_str;
	size_t	len1;
	size_t	len2;
	size_t	i;
	size_t	j;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	joined_str = (char *)malloc((len1 + len2 + 1) * sizeof(char));
	if (!joined_str)
		return (NULL);
	i = -1;
	while (i++, i < len1)
		joined_str[i] = s1[i];
	j = -1;
	while (j++, j < len2)
		joined_str[i + j] = s2[j];
	joined_str[i + j] = '\0';
	return (joined_str);
}
